	// 所有词统一转小写，便于不区分大小写匹配
	private val BANNED_LOWER: List<String> = BANNED_WORDS.map { it.lowercase() }

	// ---------------------------------------------------------------
	// 格式校验
	// ---------------------------------------------------------------

	/** 用户名：仅允许中文、英文字母、数字，长度 2-20 */
	fun isValidUsername(s: String): Boolean =
		s.matches(Regex("[\\u4e00-\\u9fa5a-zA-Z0-9]{2,20}"))

	/** 姓名（nickname）：仅允许 2-4 个汉字 */
	fun isValidNickname(s: String): Boolean =
		s.matches(Regex("[\\u4e00-\\u9fa5]{2,4}"))

	/** 分组名：允许中文、英文字母、数字及常见分隔符，长度 1-20 */
	fun isValidGroupName(s: String): Boolean =
		s.matches(Regex("[\\u4e00-\\u9fa5a-zA-Z0-9 _\\-()（）]{1,20}"))

	// ---------------------------------------------------------------
	// 敏感词检测
	// ---------------------------------------------------------------

	/** 判断字符串是否包含敏感词（忽略大小写） */
	fun containsBannedWord(s: String): Boolean {
		val lower = s.lowercase()
		return BANNED_LOWER.any { lower.contains(it) }
	}

	// ---------------------------------------------------------------
	// 注册统一入口
	// ---------------------------------------------------------------

	/**
	 * 检查注册字段，返回第一个错误提示，无误则返回 null
	 */
	fun checkRegisterInput(username: String, nickname: String, groupName: String): String? {
		if (!isValidUsername(username)) {
			return "用户名只能包含中文、英文字母和数字，长度2-20个字符"
		}
		if (nickname.isNotEmpty() && !isValidNickname(nickname)) {
			return "姓名必须为2-4个汉字"
		}
		if (groupName.isNotEmpty() && !isValidGroupName(groupName)) {
			return "分组名只能包含中文、英文字母、数字和常见符号，长度1-20个字符"
		}
		if (containsBannedWord(username)) return "用户名含有违禁词汇，请重新填写"
		if (nickname.isNotEmpty() && containsBannedWord(nickname)) return "姓名含有违禁词汇，请重新填写"
		if (groupName.isNotEmpty() && containsBannedWord(groupName)) return "分组名含有违禁词汇，请重新填写"
		return null
	}
